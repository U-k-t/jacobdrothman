/**
 * Complete real travel history, December 2004 – June 2026. Ordered chronologically ascending.
 * Route legs are *generated* from this list by `routeLegs.ts`, not hand-duplicated here. See
 * plan.md ("Full Dataset Replacement Pass") for coordinate approximations, the Southern
 * California naming decision, and same-month ordering notes.
 *
 * Every journey has a real, known month — no fallback-month cases remain in this dataset
 * (`timing.ts`'s fallback logic is kept for robustness against future entries, not because it's
 * needed here). Every leg has a real, known travel mode — nothing is left unresolved.
 */

export interface JourneyStop {
  location: Location;
  /** Mode used to travel from the previous point (the origin, or the prior stop) to this one. */
  travelModeFromPrevious?: TravelMode;
  /** Only set when a specific stop's date is individually known (rare — see plan.md). */
  month?: number;
  year?: number;
}

export interface Journey {
  /** Stable unique id. */
  id: string;
  /** 1-based chronological visit order. */
  order: number;
  origin: Location;
  start: {
    year: number;
    /** 1–12. Left `undefined` only if the source data ever lacks a month — never guessed. */
    month?: number;
    /** Disambiguates multiple journeys sharing the same year+month, in listed order. */
    sequence?: number;
  };
  /** One or more ordered stops after the origin. */
  stops: JourneyStop[];
  /**
   * Required (never optional/defaulted) so a return leg is only ever generated when the
   * journey definition explicitly says the traveler returned to the origin.
   */
  returnToOrigin: boolean;
  /** Mode for the final stop -> origin leg. Only meaningful when returnToOrigin is true. */
  returnTravelMode?: TravelMode;
  /** Descriptive only — which temporary hub era this journey belongs to, if any. */
  hubId?: "villach" | "munich" | "san-francisco";
  caption?: string;
}

